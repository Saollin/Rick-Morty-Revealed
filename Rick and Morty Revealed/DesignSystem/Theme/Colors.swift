import SwiftUI

extension Color {
    static var rmPrimary: Color  {
        return Color("PrimaryColor")
    }
    static var rmWhite: Color  {
        return Color("BackgroundColor")
    }
    static var rmNavy: Color  {
        return Color("SecondaryColor")
    }
    static var rmYellow: Color  {
        return Color("TextPrimaryColor")
    }
    static var rmBrown: Color  {
        return Color("TextSecondaryColor")
    }
    static var rmError: Color {
        return Color("ErrorColor")
    }
}
